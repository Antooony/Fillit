void		ft_check_tetri(t_list *lst)
{
	int			i;

	i = 0;
	while (i < 19)
	{
		if (!ft_strcmp(g_table[i], lst->content))
			break ;
		i++;
	}
	if (i == 19)
	{
		ft_putstr_fd("error", 2);
		exit(1);
	}
}

int			ft_get_i(t_list *lst)
{
	int			i;

	i = 0;
	while (i < 19)
	{
		if (!ft_strcmp(g_table[i], lst->content))
			break ;
		i++;
	}
	if (i == 19)
	{
		ft_putstr_fd("error", 2);
		exit(1);
	}
	return (i);
}

void		ft_getox(t_list *lst)
{
	static int	c = 'A';

	lst->content = g_matrice[ft_get_i(lst)];
	lst->letter = c;
	c++;
}
